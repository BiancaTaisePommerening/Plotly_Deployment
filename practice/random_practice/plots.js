// • The first argument in Plotly,newPlot() is "plotArea". 
// Recall that this corresponds to the ID of the <div> tag in the HTML document.
// • The second argument is an array, as indicated by the square brackets. 
// Inside the array is an object, as notated by the curly brackets, in which values of x and y are specified. 
// The x and y values are contained inside arrays as well.
Plotly.newPlot("plotArea", [{x: [1, 2, 3], y: [10, 20, 30]}]);

